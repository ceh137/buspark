    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\RedirectResponse
     */
    public function destroy($id)
    {
        $ch =CheckList::find($id);
        $questions = CheckQuestion::where('check_list_id', '=', $id)->get('id');
        Answer::whereIn('check_question_id', $questions)->delete();
        CheckQuestion::where('check_list_id', '=', $id)->delete();
        CheckListSession::where('check_list_id', '=', $id)->delete();
        $ch->delete();
        return redirect()->back();
    }
